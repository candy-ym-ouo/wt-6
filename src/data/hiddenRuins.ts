export function getRuinsById(id: string): RuinsConfig | undefined {
  return hiddenRuins.find(r => r.id === id);
}

export function getRuinsForChapter(chapterId: string): RuinsConfig[] {
  return hiddenRuins.filter(r => r.chapterId === chapterId);
}

export function getRuinsReward(id: string): RuinsReward | undefined {
  return RUINS_REWARDS[id];
}

export function getAllRuinsRewards(): Record<string, RuinsReward> {
  return { ...RUINS_REWARDS };
}
